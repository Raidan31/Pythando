Saldo = 500
Saque = 200

print (Saldo == Saque) #Afirmou que Saque é igual a Saldo por sererem diferentes imprimiu False

print (Saldo != Saque) #Afirmou que Saque é diferente de Saldo por serem diferente imprimiu Verdadeiro

print (Saldo > Saque) #Afirmou que Saldo é maior que Saque por Saldo ser maior imprimiu Verdadeiro

print (Saldo >= Saque) #Afirmou que Saldo é maior ou igual ao Saque por Saldo ser maior imprimiu Verdadeiro

print (Saldo < Saque) #Afirmou que Saldo é menor que Saque por Saldo ser maior imprimiu Falso

print (Saldo <= Saque) #Afirmou que Saldo é menor ou igual ao Saque por Saldo ser maior imprimiu Falso
