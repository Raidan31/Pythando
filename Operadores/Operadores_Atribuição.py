#São Operadores utilizados para definir o valor inicial ou sobrescrever o valor de uma variavel


print("Adição")

saldo = 500 #Atribuição de Variavel Simples
print(saldo)
saldo += 200 #Atribuição de Variavel com Adição de um outro valor 
print(saldo)

print("Subtração")

saldo = 500 #Atribuição de Variavel Simples
print(saldo)
saldo -= 200 #Atribuição de Variavel com Subtração de um outro valor 
print(saldo)

print("Multiplicação")

saldo = 500 #Atribuição de Variavel Simples
print(saldo)
saldo *= 200 #Atribuição de Variavel com a multiplicação de um outro valor 
print(saldo)

print("Divisão")

saldo = 500 #Atribuição de Variavel Simples
print(saldo)
saldo /= 3 #Atribuição de Variavel com a divisão de um outro valor 
print(saldo)


print("Divisão Sem números quebrados")

saldo = 500 #Atribuição de Variavel Simples
print(saldo)
saldo //= 3 #Atribuição de Variavel com a divisão sem números quebrados de um outro valor 
print(saldo)

print("Modulo - Resto da Divisão")

saldo = 500 #Atribuição de Variavel Simples
print(saldo)
saldo %= 480 #Atribuição de Variavel que demonstra o resto da divisão entre esses dois valores (500 e 480)
print(saldo)

print("Exponenciação")

saldo = 10 #Atribuição de Variavel Simples
print(saldo)
saldo **= 20 #Atribuição de Variavel com a exponenciação por um outro valor 
print(saldo)