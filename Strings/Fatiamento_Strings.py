nome = "Raidan Nunes de Oliveira"

print (nome [0])# Por começar a partir do 0 imprimiu a letra R 

print (nome [:6])# Vai pegar todas as letras e os espaços até o numero 6

print (nome [6:])# Vai pegar todos os caracter partindo do 6 em diante

print (nome [7:12])# Pegou os caracteres especificos dentro desse espaço

print (nome [7:12:2])# O 2 esta atuando como step (de dois em dois)

print (nome [:])# Retorna a cópia da String

print (nome [::-1])# Espelha a String




